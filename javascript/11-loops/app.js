//* For Loop
for(let i=0; i<10;i++){
    console.log("For loop " + i)
}

//* While Loop
let counter = 0;
while(counter < 10) {
    console.log("While loop " + counter);
    counter++;
}

//* Do While
let condition = 0;
do {
    console.log("Do while loop " + condition)
    condition++;
} while (condition < 10);
